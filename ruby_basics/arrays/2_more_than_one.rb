pets = ['cat', 'dog', 'fish', 'lizard']

p my_pets = pets[2..3]

puts "I have a pet #{my_pets[0]} and a pet #{my_pets[1]}!"

=begin

You can use 'range of numbers'. 
array[2..5]  selects array elements between 2 and 5.
Can be use to select multiple elements in a array

Benefits:
  -instead of typeing petes[1],pets[2],pets[3]
  you can type pets[1..3].
  much quicker and easier to read.

=end
